-- 문제 3
SELECT 
	 DECODE(SUBSTR(STUDENT_SSN, 8, 1),
					'2', '여자' ))
FROM TB_DEPARTMENT
WHERE DEPARTMENT_NO =001 
  AND ABSENCE_YN = 'Y';
 
 
-- 문제 4
SELECT STUDENT_NAME
FROM TB_STUDENT
WHERE STUDENT_NO IN ('A513079', 'A513090', 'A513091', 'A513110', 'A513119')
ORDER BY STUDENT_NAME DESC; 

-- 문제 5
SELECT 
		DEPARTMENT_NAME,
		CATEGORY
FROM TB_DEPARTMENT
WHERE CAPACITY >= 20 AND CAPACITY <= 30;

-- 문제 6
