-- 문제 3
SELECT
    STUDENT_NAME AS "학생이름",
    STUDENT_NO AS "학번",
    STUDENT_ADDRESS AS "거주지 주소"
FROM TB_STUDENT
WHERE (STUDENT_ADDRESS LIKE '경%' OR STUDENT_ADDRESS LIKE '강%')
  AND STUDENT_NO LIKE '9%'
ORDER BY STUDENT_NAME ASC;


